// Observing packets across the network and performance analysis
// Wireshark: Wireshark is a network protocol analyser that can be installed on Windows, 
// Linux and Mac. It provides a comprehensive capture and is more informative than Fiddler.
// How To use:
// 1. Install Wireshark .
// 2. Open your Internet browser.
// 3. Clear your browser cache.
// 4. Open Wireshark
// 5. Click on "Capture > Interfaces". A pop up window will show up. (You probably want to 
// capture traffic that goes through your ethernet driver.)
// 6. Click on the Start button to start capturing traffic via this interface.
// 7. Visit the URL that you wanted to capture the traffic from.
// 8. Click on stop and take screenshot of the readings.